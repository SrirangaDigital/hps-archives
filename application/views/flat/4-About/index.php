            <h1>About</h1>
            <p>The Hyderabad Public School is a co-educational, day & residential school in Hyderabad. One of the oldest educational institutions in the country in the youngest state of the country – it is this range that best defines The Hyderabad Public School.</p>

            <p>In 1919, a proposal was presented to the Seventh Nizam, H.E.H. Mir Osman Ali Khan by the Hon’ble H. Weikfield, Director General of the Court of Wards (Department of Revenue) to establish a fully residential school on the lines of Eton College, London, to give the sons of Jagirdars an enriching learning experience.</p>


            <p>In 1923, the Jagirdar’s College opened its doors to five students and six teachers under the first Founder-Principal Mr.H.W.Shawcross. The first batch appeared for Senior Cambridge ‘O’ levels in 1929. By 1930, the strength of the School had risen to 150.</p>            
